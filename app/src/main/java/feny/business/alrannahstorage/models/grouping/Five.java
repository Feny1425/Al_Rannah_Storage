package feny.business.alrannahstorage.models.grouping;

public class Five<T,E,B,A,D> {
    public T first;
    public E second;
    public B third;
    public A fourth;
    public D fifth;

    public Five(T first, E second, B third, A fourth, D fifth) {
        this.first = first;
        this.second = second;
        this.third = third;
        this.fourth = fourth;
        this.fifth = fifth;
    }
}